class AdminPanelProvider extends PanelProvider
{

    

    public function panel(Panel $panel): Panel
    {
        return $panel
            ->default()
            ->id('admin')
            ->path('admin')
            ->globalSearch(false)

            ->brandLogo('/images/logo-without-bg.png')
            ->brandName('SmartCare')

            ->font('Cairo')
        
            ->login()
            ->colors([
                'primary' => Color::Blue,
                'success' => Color::Green,
                'info' => Color::Cyan,
                'warning' => Color::Yellow,
                'danger' => Color::Red,
            ])
            ->discoverResources(in: app_path('Filament/Resources'), for: 'App\Filament\Resources')
            ->discoverPages(in: app_path('Filament/Pages'), for: 'App\Filament\Pages')
            ->pages([
                Dashboard::class,
            ])
            ->discoverWidgets(in: app_path('Filament/Widgets'), for: 'App\Filament\Widgets')
            ->widgets([
                AccountWidget::class,

             //   \App\Filament\Widgets\AiChatButton::class, // إضافة الدائرة

     

            ])
            ->middleware([
                EncryptCookies::class,
                AddQueuedCookiesToResponse::class,
                StartSession::class,
                AuthenticateSession::class,
                ShareErrorsFromSession::class,
                VerifyCsrfToken::class,
                SubstituteBindings::class,
                DisableBladeIconComponents::class,
                DispatchServingFilamentEvent::class,
            ])
            ->authMiddleware([
                Authenticate::class,
            ]);
    }
    

    protected function getWidgets(): array
    {
        return [
            PatientVitalsChart::class,
            \App\Filament\Widgets\FixedNoteWidget::class,

           // PatientVitalsWidget::class,
            AiChatButton::class, // ✅ تأكد من إضافة الودجت هنا
        ];
    }


    


    


}
